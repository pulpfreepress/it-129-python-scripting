import unittest
from context import Example

class Test_Example(unittest.TestCase):

	def test_increment(self):
		example = Example()
		assert example.get_count() == 0
		example.increment_count()
		assert example.get_count() == 1


	def test_sum(self):
		example = Example()
		assert example.sum(1, 1) == 2
		assert example.sum(1,2) == 3